// Simular el procesamiento en lotes del hook
async function procesarDenunciasEnLotes(promesas, tama√±oLote = 3, delayMs = 1000) {
  const resultados = [];
  
  console.log(`üì¶ Procesando ${promesas.length} denuncias en lotes de ${tama√±oLote}`);
  
  for (let i = 0; i < promesas.length; i += tama√±oLote) {
    const lote = promesas.slice(i, i + tama√±oLote);
    const loteNumero = Math.floor(i / tama√±oLote) + 1;
    const totalLotes = Math.ceil(promesas.length / tama√±oLote);
    
    console.log(`üîÑ Procesando lote ${loteNumero}/${totalLotes}`);
    
    try {
      const resultadosLote = await Promise.all(lote);
      const denunciasValidas = resultadosLote.filter(d => d !== null);
      resultados.push(...denunciasValidas);
      
      console.log(`‚úÖ Lote ${loteNumero} completado: ${denunciasValidas.length}/${lote.length} v√°lidas`);
      
      // Delay entre lotes
      if (i + tama√±oLote < promesas.length) {
        console.log(`‚è≥ Esperando ${delayMs}ms...`);
        await new Promise(resolve => setTimeout(resolve, delayMs));
      }
    } catch (error) {
      console.error(`‚ùå Error en lote ${loteNumero}:`, error.message);
      continue;
    }
  }
  
  return resultados;
}

// Simular exactamente la funci√≥n obtenerDenuncias del hook
async function obtenerDenuncias() {
  try {
    console.log('üöÄ SIMULANDO obtenerDenuncias DEL HOOK');
    console.log('=====================================');
    
    // Simular setLoading(true)
    console.log('üîÑ Estado: loading = true');
    
    // Simular setError(null)
    console.log('üîÑ Estado: error = null');

    console.log('üöÄ OBTENIENDO DENUNCIAS SIN METAMASK');
    
    // Usar provider p√∫blico (no requiere MetaMask)
    const provider = await getPublicProvider();
    const contract = new ethers.Contract(CONTRACT_ADDRESS, DENUNCIA_ABI, provider);

    console.log('Obteniendo total de denuncias...');
    
    try {
      // Obtener el total de denuncias
      console.log('üîç Consultando contrato en:', CONTRACT_ADDRESS);
      const total = await contract.totalDenuncias();
      const totalNumber = Number(total);
      
      console.log(`‚úÖ Total de denuncias en el contrato: ${totalNumber}`);

      if (totalNumber === 0) {
        console.log('üìù Contrato nuevo - no hay denuncias registradas a√∫n');
        console.log('üîÑ Estado: loading = false');
        console.log('üìä Estado: denuncias = []');
        return [];
      }

      // Obtener todas las denuncias (limitado a 10 para testing)
      const maxToProcess = Math.min(totalNumber, 10);
      console.log(`üîç Procesando las primeras ${maxToProcess} denuncias...`);
      
      const denunciasPromises = [];
      for (let i = 0; i < maxToProcess; i++) {
        denunciasPromises.push(
          contract.obtenerDenuncia(i).then(async (denunciaStruct) => {
            try {
              console.log(`üìã Procesando denuncia ${i}:`, {
                denunciante: denunciaStruct.denunciante.slice(0, 10) + '...',
                tipoAcoso: denunciaStruct.tipoAcoso,
                ipfsHash: denunciaStruct.ipfsHash.slice(0, 15) + '...'
              });

              // Simular obtenci√≥n del bloque
              const currentBlock = await provider.getBlockNumber();
              
              // Simular preview de descripci√≥n
              let descripcionPreview = "No se proporcion√≥ descripci√≥n";
              if (denunciaStruct.ipfsHash) {
                // Simular que no podemos obtener IPFS en este test
                descripcionPreview = "Contenido almacenado en IPFS (haz clic en 'Ver descripci√≥n completa' para acceder)";
              }

              const denuncia = {
                denunciante: denunciaStruct.denunciante,
                tipoAcoso: denunciaStruct.tipoAcoso,
                descripcion: descripcionPreview,
                ipfsHash: denunciaStruct.ipfsHash,
                proof: denunciaStruct.proof,
                publicSignals: denunciaStruct.publicSignals,
                timestamp: new Date(Number(denunciaStruct.timestamp) * 1000),
                blockNumber: currentBlock,
                esPublica: denunciaStruct.esPublica !== undefined ? denunciaStruct.esPublica : true
              };
              
              console.log(`‚úÖ Denuncia ${i} procesada exitosamente`);
              return denuncia;
            } catch (error) {
              console.error(`‚ùå Error al procesar denuncia ${i}:`, error.message);
              return null;
            }
          }).catch((error) => {
            console.error(`‚ùå Error al obtener denuncia ${i}:`, error.message);
            return null;
          })
        );
      }

      // Procesar denuncias en lotes
      const denunciasValidas = await procesarDenunciasEnLotes(denunciasPromises, 3, 1000);
      
      console.log(`‚úÖ Se obtuvieron ${denunciasValidas.length} denuncias v√°lidas`);
      
      // Ordenar las denuncias por timestamp (m√°s recientes primero)
      const denunciasOrdenadas = denunciasValidas.sort((a, b) => b.timestamp.getTime() - a.timestamp.getTime());
      
      console.log('üîÑ Estado: loading = false');
      console.log(`üìä Estado: denuncias = [${denunciasOrdenadas.length} elementos]`);
      
      return denunciasOrdenadas;
      
    } catch (contractError) {
      console.error('‚ùå Error al llamar funciones del contrato:', contractError.message);
      
      // Si es un contrato nuevo, es normal que no tenga denuncias
      if (contractError.message.includes('could not decode result data')) {
        console.log('üìù Contrato nuevo detectado - inicializando historial vac√≠o');
        console.log('üîÑ Estado: loading = false');
        console.log('üìä Estado: denuncias = []');
        return [];
      }
      
      // Fallback: intentar obtener eventos
      console.log('üîÑ Intentando obtener denuncias mediante eventos...');
      console.log('‚ö†Ô∏è Fallback no implementado en este test');
      console.log('üîÑ Estado: loading = false');
      console.log('üìä Estado: denuncias = []');
      return [];
    }
  } catch (err) {
    console.error('‚ùå ERROR CR√çTICO en obtenerDenuncias:', err.message);
    console.log('üîÑ Estado: loading = false');
    console.log('üîÑ Estado: error =', err.message);
    return [];
  }
}

